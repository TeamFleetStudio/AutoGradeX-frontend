"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

/**
 * Bottom navigation bar for mobile devices
 * Shows key navigation items at the bottom of the screen
 */
export function MobileBottomNav({ items, basePath }) {
  const pathname = usePathname();
  
  // Only show first 5 items max
  const displayItems = items.slice(0, 5);
  
  return (
    <nav className="lg:hidden fixed bottom-0 left-0 right-0 z-50 bg-card border-t border-border safe-area-pb">
      <div className="flex items-center justify-around h-16">
        {displayItems.map((item) => {
          const isActive = pathname === item.href || 
            (item.href !== basePath && pathname.startsWith(item.href));
          
          return (
            <Link
              key={item.id}
              href={item.href}
              className={`flex flex-col items-center justify-center flex-1 h-full transition-colors ${
                isActive
                  ? 'text-primary'
                  : 'text-muted-foreground hover:text-foreground'
              }`}
            >
              <MobileNavIcon icon={item.icon} isActive={isActive} />
              <span className="text-xs mt-1 font-medium">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

